    //metodo que devuelve 1 si existe un alojamiento en la ciudad ingresada en el jtextfield de busqueda    
    public int existe(String ciudad){
        Integer result=0;
        try{
            Statement ejecutor=conn.createStatement();
            //consulta sql que selecciona la entidad de la base de datos cuando la entidad es igual a la ingresada en el campo de busqueda
            ResultSet rs=ejecutor.executeQuery("SELECT entidad FROM alojamiento WHERE entidad='"+ciudad+"'");
            if(rs.next()){
                ciudadBase.setText(rs.getString("entidad"));  //Guarda la entidad
                String ciudadEnBase=ciudadBase.getText();
                System.out.println("esto tiene ciudad ingresada:"+ciudad);
                System.out.println("esto tiene base de datos:"+ciudadEnBase);                     
                //compara las cadenas y si son iguales entonces retorna un 1
                if(ciudad.equals(ciudadEnBase)){
                    result=1;
               }else{//si no son iguales retorna un 0 entonces no hay alojamientos en esa entidad
                   System.out.println("entra aqui:"+ciudad);
                   result=0;
               }    
            }else{
                JOptionPane.showMessageDialog(null,"No existen alojamientos para esta entidad federativa");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Error al conectar"+e.getMessage(),e.getMessage(),JOptionPane.ERROR_MESSAGE);
        }
        return result;
    }
