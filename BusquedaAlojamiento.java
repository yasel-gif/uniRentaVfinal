        public void muestraAlojamientosPuntaje(){
        modelo = new DefaultTableModel();
        File archivo;
        try{
            DefaultTableModel modelo =new DefaultTableModel();
            tablaAlojamientoss.setModel(modelo);
            BaseDeDatosConexion con=new BaseDeDatosConexion();
            Connection conn=con.getConection();
            //Consulta sql que muestra en orden descendente los alojamientos existentes
            ps=conn.prepareStatement("SELECT id_alojamiento,titulo,entidad,puntaje FROM alojamiento order by puntaje desc");
            rs=ps.executeQuery();
            ResultSetMetaData rsMd = rs.getMetaData();
            int cantColumnas=rsMd.getColumnCount();
            //crea las colunas y les da un titulo
            modelo.addColumn("Id_Alojamiento");
            modelo.addColumn("Titulo");
            modelo.addColumn("entidad");
            modelo.addColumn("puntaje");
            //modelo.addColumn("Imagen");
            this.tablaAlojamientoss.setModel(modelo);
            while(rs.next()){
                Object[] filas=new Object[18];
                //ciclo que muestra los datos de la base de datos obtenidos, de acuerdo al orden establecido
                for(int i=0; i<cantColumnas; i++){
                    filas[i]=rs.getObject(i+1);
                }    
                modelo.addRow(filas);
            }
        }
        catch(SQLException exp){
            System.err.println(exp.toString());
        }
    }
               
