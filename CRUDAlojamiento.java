    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed
        try{
            BaseDeDatosConexion objCon=new BaseDeDatosConexion();
            Connection conn=objCon.getConection();     
            //Consulta sql donde inserta un nuevo alojamiento con todos sus campos de acuerdo al arrendador_id
            ps=conn.prepareStatement("INSERT INTO alojamiento( titulo,entidad,municipio,cp,dir,numHabs,costo,servicios,tipo,detalles,arrendador_id,disponibilidad,imagen) VALUES(?,?,?,?,?,?,?,?,?,?,?,?,?)"); 
            ps.setString(1,txtTitulo.getText());
            ps.setString(2,txtEntidad.getText());
            ps.setString(3,txtCiudad.getText());
            ps.setString(4,txtCP.getText());
            ps.setString(5,txtDir.getText());
            ps.setString(6,txtNumHabs.getText());
            ps.setString(7,txtCosto.getText());
            ps.setString(8,txtServicios.getText());
            ps.setString(9,txtTipo.getText());
            ps.setString(10,txtDetalles.getText());
            ps.setInt(11,Integer.parseInt(recibeIdArr2.getText()));
            ps.setString(12,txtDisp.getText());
            ps.setString(13,txtImagen.getText());
            
            int res=ps.executeUpdate();
            if(res>0){
                JOptionPane.showMessageDialog(null, "Datos de alojamiento guardados");
                limpiar();
                mostrar(); 
            }else{
                JOptionPane.showMessageDialog(null, "Error al guardar datos");
            }
            conn.close();
        }
        catch(HeadlessException | SQLException e){
           JOptionPane.showMessageDialog(null, "Ingrese los datos correctamente");
        } 
    }//GEN-LAST:event_btnInsertarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        
        try{
            BaseDeDatosConexion con=new BaseDeDatosConexion();
            Connection conn=con.getConection();
            //Consulta sql que selecciona todos los campos de la tabla alojamiento de acuerdo al id_alojamiento
            ps=conn.prepareStatement("SELECT * FROM alojamiento WHERE id_alojamiento = ?");
            int ide=Integer.parseInt(idMod.getText());
            ps.setInt(1,ide);
            rs=ps.executeQuery();
            if(rs.next()){               
                //Muestra en cada jtextfield los datos recibidos de la base de datos para posteriormente ser modificados y gusardados
                txtTitulo.setText(rs.getString("titulo"));
                txtEntidad.setText(rs.getString("entidad"));
                txtCiudad.setText(rs.getString("municipio"));
                txtCP.setText(rs.getString("cp"));
                txtDir.setText(rs.getString("dir"));
                txtNumHabs.setText(rs.getString("numHabs"));
                txtCosto.setText(rs.getString("costo"));
                txtServicios.setText(rs.getString("servicios"));
                txtTipo.setText(rs.getString("tipo"));
                txtDetalles.setText(rs.getString("detalles"));
                recibeIdArr2.setText(rs.getString("arrendador_id")); 
                txtDisp.setText(rs.getString("disponibilidad")); 
                txtImagen.setText(rs.getString("imagen")); 
                //Muestra los datos en la tabla
                mostrar();
            }else{
                JOptionPane.showMessageDialog(null,"No existe el usuario");
                mostrar();
            }
             //Cierra la conexion
             conn.close();
        }catch(HeadlessException | SQLException e){
           JOptionPane.showMessageDialog(null,"No existe el usuario");
        }
    }//GEN-LAST:event_btnModificarActionPerformed


