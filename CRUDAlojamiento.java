    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
    
        try{
            BaseDeDatosConexion con=new BaseDeDatosConexion();
            Connection conn=con.getConection();
            //Consulta sql que elimina un alojamiento de acuerdo al id_alojamiento ingresado
            ps=conn.prepareStatement("DELETE FROM alojamiento WHERE id_alojamiento=?");
            ps.setInt(1,Integer.parseInt(idAl.getText()));            
            int res=ps.executeUpdate();            
            if(res>0){
                JOptionPane.showMessageDialog(null, "Alojamiento eliminado");
                //muestra la tabla de alojamientos despues de eliminar uno.
                mostrar();
                limpiar();
            }else{
                JOptionPane.showMessageDialog(null, "Error al eliminar alojamiento");
                mostrar();
                limpiar();
            }
        //Cierra la conexion.    
        conn.close();
        }
        catch(HeadlessException | SQLException e){
            JOptionPane.showMessageDialog(null,"Ingrese un id correcto");
        }    
    }//GEN-LAST:event_btnEliminarActionPerformed

