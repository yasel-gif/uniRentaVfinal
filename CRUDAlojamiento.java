    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        PreparedStatement ps=null;
        ResultSet rs=null;
        try{
            BaseDeDatosConexion objCon=new BaseDeDatosConexion();
            Connection conn=objCon.getConection();  
            //Consulta sql que guarda cambios realizados en algun alojamiento de acuerdo al id_alojamiento
            ps=conn.prepareStatement("UPDATE alojamiento SET titulo=?, entidad=?,municipio=?,cp=?,dir=?,numHabs=?,costo=?,servicios=?,tipo=?,detalles=?,arrendador_id=?, disponibilidad=?,imagen=? WHERE id_alojamiento=?"); 
            ps.setString(1,txtTitulo.getText());
            ps.setString(2,txtEntidad.getText());
            ps.setString(3,txtCiudad.getText());
            ps.setString(4,txtCP.getText());
            ps.setString(5,txtDir.getText());
            ps.setString(6,txtNumHabs.getText());
            ps.setString(7,txtCosto.getText());
            ps.setString(8,txtServicios.getText());
            ps.setString(9,txtTipo.getText());
            ps.setString(10,txtDetalles.getText());
            ps.setInt(11,Integer.parseInt(recibeIdArr2.getText()));
            ps.setString(12,txtDisp.getText());
            ps.setString(13,txtImagen.getText());
            ps.setInt(14,Integer.parseInt(idMod.getText())); 
            int res=ps.executeUpdate();
            //muestra los datos modificadoe
            mostrar();  
            if(res>0){
                JOptionPane.showMessageDialog(null, "Datos modificados correctamente");
                limpiar();
            }else{
                JOptionPane.showMessageDialog(null, "Error al guardar datos");
                limpiar();
            }
            //Cierra la conexion
            conn.close();
        }
        catch(HeadlessException | SQLException e){
           System.err.print(e);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

